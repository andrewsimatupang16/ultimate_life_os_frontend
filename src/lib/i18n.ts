function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function translatePhrases(value: string) {
  return Object.entries(staticTranslations)
    .filter(([source, target]) => source.length > 2 && source !== target && /[\s.,:?()]/.test(source))
    .sort(([a], [b]) => b.length - a.length)
    .reduce((result, [source, target]) => result.replace(new RegExp(escapeRegExp(source), 'g'), target), value);
}

export function translateStaticText(value: string, language: Language) {
  if (language === 'id') return value;
  const trimmed = value.trim();
  const translated = staticTranslations[trimmed] ?? translatePhrases(trimmed);
  return translated === trimmed ? value : withOriginalWhitespace(value, translated);
}

export function withOriginalWhitespace(original: string, translated: string) {
  const leading = original.match(/^\s*/)?.[0] ?? '';
  const trailing = original.match(/\s*$/)?.[0] ?? '';
  return `${leading}${translated}${trailing}`;
}
